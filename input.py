# arithmetic operator
x = 10
y = 12
add = x+y
print(add)
sub = x-y
print(sub)
mul = x*y
print(mul)
div = x/y
print(div)
p = x**2
print(p)
mod = x % y
print(mod)
# logical operator
A = True
B = False
print(A and B)
print(A or B)
print(not A)
# bitwise operator
# compliment operator
M = 10
N = 4
print(M & N)
print(M | N)
print(~N)
print(M ^ N)
print(M << N)
print(M >> N)
